      <h1 class="fw-bold">Tranexamic Acid Tidak Boleh Dicampur Dengan Kandungan Berikut Ini</h1>
    </header>

    <p class="fs-5">
      Bestie, ada begitu banyak ingredients yang digunakan dalam produk skincare, salah satunya adalah
      tranexamic acid. Sama seperti bahan aktif bersifat asam lainnya, tranexamic acid tidak boleh dicampur dengan
      beberapa ingredients lain untuk mencegah terjadinya reaksi berbahaya pada kulit seperti iritasi.
    </p>
    <p class="fs-5">
      Tranexamic acid memiliki beberapa manfaat untuk kulit, antara lain mencerahkan, mengurangi melasma, serta
      bintik hitam. Karena manfaat inilah tranexamic acid kerap ditambahkan dalam skincare. Sayangnya, kandungan ini
      kurang familiar jika dibandingkan dengan niacinamide, ceramide, centella asiatica, salicylic acid dan lainnya.
    </p>
    <p class="fs-5">
      Saya akan membahas lebih dalam tentang kandungan yang mungkin masih asing bagi kamu. Yuk, cek berikut ini
    </p>

    <p class="fs-5">Kalau kamu menggunakan skincare yang mengandung ingredient berupa tranexamic acid, pastikan
      untuk tidak
      menggunakannya secara bersamaan dengan beberapa ingredient berikut.</p>

    <header class="mt-5">
      <h3 class="fw-bold">1. Retinol</h3>
    </header>
    <p class="fs-5">Retinol berfungsi untuk mempercepat proses regenerasi kulit melalui proses eksfoliasi.
      Sedangkan tranexamic
      acid juga memiliki fungsi eksfoliasi. Jika menggunakannya secara bersamaan, efeknya akan terlalu keras untuk
      kulit.</p>
    <p class="fs-5">
      Alih-alih bikin kulit cerah, justru kamu akan mendapatkan masalah baru. Misalnya kulit menjadi sangat kering,
      kemerahan, hingga mengelupas pada kasus yang sudah lebih parah. Kalau memang ingin menggunakan dua ingredients
      tersebut sekaligus, mintalah saran dari dokter kulit.
    </p>



    <header class="mt-5">
      <h3 class="fw-bold">2. Vitamin C</h3>
    </header>
    <p class="fs-5">Vitamin C terkenal sebagai antioksidan yang melindungi kulit dari kerusakan. Namun
      mengombinasikannya dengan tranexamic acid sungguh bukan ide yang bagus. Penggunaannya bersamaan membuat pH
      kulit menjadi tidak seimbang sehingga penyerapan kedua ingredients jadi tidak maksimal.</p>
    <p class="fs-5">
      Selain itu, campuran vitamin C dan tranexamic acid juga memicu reaksi alergi pada kulit. Risiko terjadinay
      iritasi juga menjadi lebih besar.
    </p>


    <header class="mt-5">
      <h3 class="fw-bold">3. AHA & BHA</h3>
    </header>
    <p class="fs-5">Terakhir, jangan campurkan tranexamic acid dengan AHA/BHA. Iritasi kulit yang serius bisa
      terjadi ketika kamu mencampurkan bahan-bahan bersifat asam ini. Pasalnya, baik AHA, BHA, maupun tranexamic
      acid sama-sama mengeksfoliasi kulit.</p>
    <p class="fs-5 mb-5">
      Menggunakannya sekaligus juga dapat membuat kulit menjadi sangat sensitif terhadap sinar matahari. Jika memang
      harus menggunakan AHA/BHA dengan tranexamic acid, berikan jarak 1 hingga 2 hari. Jangan lupa pakai sunscreen
      setelahnya.
    </p>
